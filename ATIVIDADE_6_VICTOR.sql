CREATE TABLE `PROFESSORES`(
`CODIGO` INT (11),
`NOME` VARCHAR (70),
`DATA_NASCIMENTO` DATE,
`DISCIPLINA` VARCHAR (70),
PRIMARY KEY (`CODIGO`)
)ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE `CATEGORIA`(
`ID_CATEGORIA` INT (11),
`DESC_CATEGORIA` VARCHAR (70),
PRIMARY KEY (`ID_CATEGORIA`)
)ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE `PRODUTOS`(
`ID_PRODUTOS` INT (11),
`DESC_PRODUTOS` VARCHAR (70),
`VALOR` FLOAT (25,2),
`ID_CATEGORIA_PRODUTOS` INT (11),
PRIMARY KEY (`ID_PRODUTOS`)
)ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE `FUNCIONARIOS`(
`ID` INT (11),
`NOME_FUNC` VARCHAR (70),
`EMAIL` VARCHAR (70),
`CPF` VARCHAR (70),
`ENDERECO` VARCHAR (70),
PRIMARY KEY (`ID`)
)ENGINE=InnoDB DEFAULT CHARSET=latin1;

