SELECT 
    p.nome AS paciente,
    m.nome AS medico,
    c.data_consulta,
    c.diagnostico,
    c.prescricao,
    i.data_internacao,
    i.data_alta,
    i.motivo AS motivo_internacao,
    t.descricao AS tratamento,
    t.data_inicio AS inicio_tratamento,
    t.data_fim AS fim_tratamento
FROM 
    pacientes p
JOIN 
    consultas c ON p.id_paciente = c.id_paciente
JOIN 
    medicos m ON c.id_medico = m.id_medico
LEFT JOIN 
    internacoes i ON p.id_paciente = i.id_paciente AND c.id_medico = i.id_medico
LEFT JOIN 
    tratamentos t ON p.id_paciente = t.id_paciente AND c.id_medico = t.id_medico;


